                <h5>Pertandingan dan rekor rubik</h5>
                <h6>Speedcubing/speedsolving</h6>
                <p><b>Speedcubing</b> adalah mencoba untuk menyelesaikan Kubus Rubik secepat mungkin.
                   Pertandingan Dunia pertama diadakan oleh Guinness Book of World Records yang diadakan di Munich pada 13 Maret 1981. Semua rubik di lubrikasi dengan ''petroleum jelly''. Pemenang resmi, dengan catatan waktu 38 detik adalah Jury Froeschl dari Munich. Kejuaraan dunia pertama internasional diadakan di Budapest pada tanggal 5 Juni 1982, dan dimenangkan oleh Minh Thai, pelajar Vietnam dari Los Angeles, dengan catatan waktu 22,95 detik. </p>
                <h6>Blindfolded solving</h6>
                <p><b>Blindfolded solving</b> (sering disingkat dengan BLD)adalah cara menyelesaikan Rubik dengan mata tertutup. Dalam menyelesaikan dengan tertutup, kontestan mememorisasi/mengingat posisi warna Rubik yang teracak dengan mata terbuka, dan kemudian ditutup matanya sebelum menyelesaikan Rubik. Waktu mulai dihitung sejak cuber melihat rubiknya dan melakukan memorisasi. rekor resmi dunia saat ini untuk 3x3x3 BLD adalah 17,20 detik oleh Jeff Park dari Amerika Serikat.

                    Selama waktu penyelesaian, peserta menggunakan penutup mata / blindfold dan seorang juri memegang sehelai kertas di bawah hidung peserta. Hal ini bertujuan agar peserta tidak dapat mengintip di bawah celah blindfold dan hidungnya. </p>
                    
                    <h6>Rekor dunia</h6>
                    <p><b>Rekor dunia</b> dalam menyelesaikan Kubus Rubik dengan mata tertutup berhasil dicetak pada kompetisi Jakarta Open 2010 pada tanggal 31 Januari 2010 di FX Building, Jakarta. Rubik berukuran 3x3 dengan mata tertutup jumlah terbanyak yaitu 16 buah dalam waktu 57 menit diselesaikan atas nama Muhammad Iril Khairul Anam dari Indonesia. Selain itu, Indonesia juga pernah memecahkan rekor dunia lain, dicabang Rubik Clock atas nama Jonathan Irvin Gunawan yang dicetaknya pada 26 May 2012 di Gedung Indonesia Menggugat, Bandung. Dia menyelesaikan Rubik Clock dengan waktu rata-rata 7.09 detik. Semua rekor ini tercatat di WCA. </p>
